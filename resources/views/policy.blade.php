    "conflicts" may arise. The following policy outlines what we define as a
    "conflict", and how these situations are resolved.
</p>
<h6>Conflicts</h6>
<p>
    Conflict exists in all over the world and, in its best form, can be an
    indicator of a positive exchange of ideas and opinions. However,
    particularly on the Internet, conflict often becomes counter-productive and
    can result in user dissatisfaction, poor services and, overall, a reduction
    in the quality of the community environment.
</p>
<p>
    At VATBot, we believe a "conflict" to be a breach any of the policies
    laid out on this page.
</p>

<h5>Resolution</h5>
<p>
    Conflicts will always be resolved by the senior members of staff with an
    oversight of the organisation in its entirety, with a view to maintaining a
    positive community environment. Staff will utilise a number of tools to
    resolve conflicts, detailed below.
</p>

<h6>Warnings</h6>
<p>
    The first method employed will be a warning. This will be communicated to
    the user by the most convenient method possible: either by email, Discord
    DM or other appropriate communication protocol. A warning will be used in
    the event of an accidental or non-malicious breach of terms. A warning will
    not have any direct implications; it is simply a reminder to the user that
    they may have breached the terms.
</p>
<h6>Bans</h6>
<p>
    Following a repeated or severe breach of the terms, it may be necessary to
    ban a user from the VATBot community. Bans vary in length, from 1 day to
    indefinite. In the event of a ban, a user will no longer have access to the
    website.
</p>

<h5>Disputes</h5>
<p>
    In the event of a ban being employed to resolve a conflict, users are
    permitted to dispute the ban provided that the duration of the ban is
    greater than 1 day.
</p>
<p>
    In order to dispute the ban, users will have to contact the community
    administrators and provide substantial evidence to support their dispute and
    prove that they have good reason to have their ban lifted.
</p>
<p>
    Disputes for bans of less than 1 day, or without substantial supporting
    evidence, will be ignored!
</p>

